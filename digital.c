void reinitArrayBool2(bool** arr) {
    int i = 0;
    int li = sizeof(arr[0])/sizeof(arr[0][0]);
    while(i < li) {
        int j = 0;
        int lj = sizeof(arr)/sizeof(arr[0][0]);
        while(j < lj) {
            arr[j][i] = 0;
            j++;
        }
        i++;
    }
}

int arrayToLedStates(bool** ledStates, bool** carac, int i) {
    int j = 0;
    int lj = sizeof(carac[0])/sizeof(carac[0][0]);
    while(j < lj) {
        int k = 0;
        int lk = sizeof(carac)/sizeof(carac[0][0]);
        while(k < lk) {
            ledStates[i][j+2] = carac[k][j];
            k++;
            i++;
        }
        j++;
    }

    return ++i;
}

